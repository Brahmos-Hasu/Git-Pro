//looping 

//for
for(i=0; i<=5;i++){
    console.log(i);
}

//while
i=6;
while(i<11){
    console.log(i);
    i++;
}

//Do while
i=9;
do {
    console.log(i);
    i--
} while (i>0);


//jump


//continue
for(i=0;i<=5;i++){
    if (i==3) {
        continue;
    }
    console.log(i);
}

//break
for(i=0;i<=5;i++){
    if (i==3) {
        break;
    }
    console.log(i);
}

